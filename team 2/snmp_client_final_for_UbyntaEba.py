# ==============================================================================
# ОПРЕДЕЛЕНИЕ OID В ФОРМАТЕ БАЙТ (BER КОДИРОВАНИЕ)
# ==============================================================================
# 
# Эти OID должны совпадать с клиентом!
# Формат: 1.3.6.1.4.1.12345.1.X.0
#
# Объяснение BER кодирования:
# - 1.3 кодируется как 1*40+3 = 43 = 0x2B
# - Числа 1-127 кодируются как есть
# - 12345 кодируется в два байта с битом продолжения:
#   12345 = 0x3039
#   В BER: (12345 >> 7) | 0x80 = 0xE0, 12345 & 0x7F = 0x79
#   Итого: 0xE0 0x79
# ==============================================================================

# OID 1.3.6.1.4.1.12345.1.1.0 - Текстовое сообщение
OID_MESSAGE = b'\x2b\x06\x01\x04\x01\xe0\x79\x01\x01\x00'

# OID 1.3.6.1.4.1.12345.1.2.0 - Счётчик
OID_COUNTER = b'\x2b\x06\x01\x04\x01\xe0\x79\x01\x02\x00'

# OID 1.3.6.1.4.1.12345.1.3.0 - Температура
OID_TEMP = b'\x2b\x06\x01\x04\x01\xe0\x79\x01\x03\x00'


# ==============================================================================
# ФУНКЦИЯ: encode_string(s)
# ==============================================================================
def encode_string(s):
    """
    Кодирует Python строку в SNMP OCTET STRING (BER формат).
    
    Формат OCTET STRING:
    ┌──────────┬─────────┬─────────────────────┐
    │ Тег 0x04 │ Длина   │ Данные (UTF-8)      │
    └──────────┴─────────┴─────────────────────┘
    
    Args:
        s (str): Строка для кодирования (например, "Hello")
        
    Returns:
        bytes: BER-кодированная строка
        
    Пример:
        >>> encode_string("Hi")
        b'\x04\x02Hi'
        # 0x04 = тег OCTET STRING
        # 0x02 = длина 2 байта
        # "Hi" = сами данные
    """
    encoded = s.encode('utf-8')  # Конвертируем Python str в bytes (UTF-8)
    # 0x04 = ASN.1 тег для OCTET STRING
    return b'\x04' + bytes([len(encoded)]) + encoded


# ==============================================================================
# ФУНКЦИЯ: encode_integer(i)
# ==============================================================================
def encode_integer(i):
    """
    Кодирует Python число в SNMP INTEGER (BER формат).
    
    Формат INTEGER:
    ┌──────────┬─────────┬──────────────────────────────────┐
    │ Тег 0x02 │ Длина   │ Значение (big-endian)            │
    └──────────┴─────────┴──────────────────────────────────┘
    
    Для чисел 0-127: длина = 1 байт
    Для чисел 128-65535: длина = 2 байта
    
    Args:
        i (int): Целое число для кодирования (0-65535)
        
    Returns:
        bytes: BER-кодированное число
        
    Примеры:
        >>> encode_integer(42)
        b'\x02\x01*'  # 0x02=тег, 0x01=длина, 0x2A=42
        
        >>> encode_integer(300)
        b'\x02\x02\x01,'  # 0x02=тег, 0x02=длина, 0x012C=300
    """
    if i < 128:
        # Однобайтовое значение
        return b'\x02\x01' + bytes([i])
    else:
        # Двухбайтовое значение (big-endian)
        # struct.pack('>H', i) - упаковывает как unsigned short, big-endian
        return b'\x02\x02' + struct.pack('>H', i)


# ==============================================================================
# ФУНКЦИЯ: create_snmp_response(request_id, oid, value, is_string)
# ==============================================================================
def create_snmp_response(request_id, oid, value, is_string=True):
    """
    Создаёт SNMP GET-RESPONSE пакет.
    
    Структура ответа:
    ┌─────────────────────────────────────────────────────────┐
    │ SEQUENCE (0x30)                                         │
    │   ├─ INTEGER version (0x02) = 0 (SNMPv1)               │
    │   ├─ OCTET STRING community (0x04) = "public"          │
    │   └─ PDU GET-RESPONSE (0xA2)                           │
    │        ├─ INTEGER request-id (копируем из запроса)     │
    │        ├─ INTEGER error-status = 0                     │
    │        ├─ INTEGER error-index = 0                      │
    │        └─ SEQUENCE variable-bindings                   │
    │             └─ SEQUENCE varbind                        │
    │                  ├─ OBJECT IDENTIFIER (OID)            │
    │                  └─ Значение (STRING или INTEGER)      │
    └─────────────────────────────────────────────────────────┘
    
    Args:
        request_id (bytes): Request ID из запроса клиента (6 байт)
                           Важно: должен совпадать с ID запроса!
        oid (bytes): OID переменной в BER формате
        value: Значение переменной (str для текста, int для числа)
        is_string (bool): True если value это текст, False если число
        
    Returns:
        bytes: Полный SNMP GET-RESPONSE пакет
        
    Пример:
        >>> response = create_snmp_response(req_id, OID_MESSAGE, "Hello", True)
        >>> sock.sendto(response, client_addr)
    """

    # --------------------------------------------------------------------------
    # Кодируем значение в зависимости от типа
    # --------------------------------------------------------------------------
    if is_string:
        value_encoded = encode_string(value)  # OCTET STRING
    else:
        value_encoded = encode_integer(value)  # INTEGER

    # --------------------------------------------------------------------------
    # Формируем VarBind (OID + значение)
    # --------------------------------------------------------------------------
    # SEQUENCE { OID, Value }
    # Длина = длина OID + длина закодированного значения + 4 (теги и длины)
    varbind = b'\x30' + bytes([len(oid) + len(value_encoded) + 4]) + \
              b'\x06' + bytes([len(oid)]) + oid + value_encoded

    # --------------------------------------------------------------------------
    # Формируем VarBindList
    # --------------------------------------------------------------------------
    varbindlist = b'\x30' + bytes([len(varbind)]) + varbind

    # --------------------------------------------------------------------------
    # Формируем PDU (GET-RESPONSE)
    # --------------------------------------------------------------------------
    error_status = b'\x02\x01\x00'  # noError (0)
    error_index = b'\x02\x01\x00'   # 0

    pdu_data = request_id + error_status + error_index + varbindlist
    # 0xA2 = тег GET-RESPONSE PDU
    pdu = b'\xa2' + bytes([len(pdu_data)]) + pdu_data

    # --------------------------------------------------------------------------
    # Формируем полное SNMP сообщение
    # --------------------------------------------------------------------------
    version = b'\x02\x01\x00'       # SNMPv1
    community = b'\x04\x06public'   # Community string "public" (6 символов)

    message_data = version + community + pdu
    message = b'\x30' + bytes([len(message_data)]) + message_data

    return message


# ==============================================================================
# ФУНКЦИЯ: parse_snmp_request(data)
# ==============================================================================
def parse_snmp_request(data):
    """
    Парсит входящий SNMP GET запрос и извлекает Request ID и OID.
    
    Это упрощённый парсер, который:
    1. Ищет Request ID по тегу 0x02 0x04 (INTEGER длиной 4 байта)
    2. Ищет OID по тегу 0x06 (OBJECT IDENTIFIER)
    
    Args:
        data (bytes): Полученный UDP пакет с SNMP запросом
        
    Returns:
        tuple: (request_id, oid) - оба в bytes
               или (None, None) если парсинг не удался
               
    Пример:
        >>> request_id, oid = parse_snmp_request(packet)
        >>> if oid == OID_MESSAGE:
        ...     response = create_snmp_response(request_id, oid, "Hello", True)
    """
    try:
        # ----------------------------------------------------------------------
        # Поиск Request ID
        # ----------------------------------------------------------------------
        # Request ID кодируется как INTEGER с длиной 4 байта: 0x02 0x04 XX XX XX XX
        idx = data.find(b'\x02\x04')  # Ищем паттерн
        if idx == -1:
            return None, None  # Не нашли - некорректный пакет

        # Извлекаем Request ID (6 байт: тег + длина + 4 байта значения)
        request_id = data[idx:idx+6]

        # ----------------------------------------------------------------------
        # Поиск OID
        # ----------------------------------------------------------------------
        # OID начинается с тега 0x06
        idx = data.find(b'\x06')
        if idx == -1:
            return None, None

        # Следующий байт после тега - длина OID
        oid_len = data[idx+1]
        
        # Извлекаем сам OID (без тега и длины)
        oid = data[idx+2:idx+2+oid_len]

        return request_id, oid
        
    except:
        # При любой ошибке возвращаем None
        return None, None


# ==============================================================================
# ФУНКЦИЯ ФОНОВОГО ОБНОВЛЕНИЯ ДАННЫХ
# ==============================================================================
def update_data():
    """
    Фоновая функция для автоматического обновления данных.
    
    Запускается в отдельном потоке (daemon thread).
    Каждые 5 секунд:
    - Увеличивает счётчик на 1
    - Генерирует новую случайную температуру (20-35)
    - Обновляет сообщение с текущим временем
    
    Поток daemon=True означает, что он завершится вместе с основной программой.
    """
    while True:
        time.sleep(5)  # Пауза 5 секунд
        
        # Инкремент счётчика
        data['counter'] += 1
        
        # Случайная температура от 20 до 35
        data['temperature'] = random.randint(20, 35)
        
        # Обновляем сообщение с текущим временем
        current_time = datetime.datetime.now().strftime('%H:%M:%S')
        data['message'] = f"Ubuntu | {current_time}"
        
        # Выводим информацию об обновлении
        print(f"[Обновление] Счётчик={data['counter']}, Темп={data['temperature']}°C")


# ==============================================================================
# ЗАПУСК ФОНОВОГО ПОТОКА ОБНОВЛЕНИЯ
# ==============================================================================
# daemon=True - поток завершится автоматически при завершении основной программы
thread = threading.Thread(target=update_data, daemon=True)
thread.start()

# ==============================================================================
# СОЗДАНИЕ И НАСТРОЙКА UDP СОКЕТА
# ==============================================================================

# Создаём UDP сокет (SOCK_DGRAM = UDP, без установления соединения)
sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)

# Привязываем сокет к адресу и порту
# После этого сокет начинает слушать входящие пакеты
sock.bind((HOST, PORT))

# ==============================================================================
# ВЫВОД ИНФОРМАЦИИ О ДОСТУПНЫХ OID
# ==============================================================================

print("\n[✓] Доступные OID:")
print("  • 1.3.6.1.4.1.12345.1.1.0 - Сообщение")
print("  • 1.3.6.1.4.1.12345.1.2.0 - Счётчик")
print("  • 1.3.6.1.4.1.12345.1.3.0 - Температура")
print("\n[✓] ГОТОВ! Ctrl+C для остановки\n")

# ==============================================================================
# ГЛАВНЫЙ ЦИКЛ ОБРАБОТКИ ЗАПРОСОВ
# ==============================================================================

try:
    while True:
        # ----------------------------------------------------------------------
        # Ожидание входящего UDP пакета
        # ----------------------------------------------------------------------
        # recvfrom() блокирует выполнение до получения пакета
        # Возвращает (данные, (ip_отправителя, порт_отправителя))
        packet, addr = sock.recvfrom(1024)

        # ----------------------------------------------------------------------
        # Парсинг SNMP запроса
        # ----------------------------------------------------------------------
        request_id, oid = parse_snmp_request(packet)

        # Проверяем, что парсинг успешен
        if request_id and oid:
            # Выводим информацию о запросе
            print(f"[←] Запрос от {addr[0]}:{addr[1]}")

            # ------------------------------------------------------------------
            # Определяем, какую переменную запрашивают, и формируем ответ
            # ------------------------------------------------------------------
            
            if oid == OID_MESSAGE:
                # Запрос текстового сообщения
                response = create_snmp_response(
                    request_id,        # Копируем ID из запроса
                    oid,               # Тот же OID
                    data['message'],   # Значение из нашего хранилища
                    True               # Это строка
                )
                print(f"[→] Отправляю: {data['message']}")
                
            elif oid == OID_COUNTER:
                # Запрос счётчика
                response = create_snmp_response(
                    request_id,
                    oid,
                    data['counter'],   # Числовое значение
                    False              # Это число
                )
                print(f"[→] Отправляю: {data['counter']}")
                
            elif oid == OID_TEMP:
                # Запрос температуры
                response = create_snmp_response(
                    request_id,
                    oid,
                    data['temperature'],
                    False
                )
                print(f"[→] Отправляю: {data['temperature']}°C")
                
            else:
                # Неизвестный OID - выводим его в hex для отладки
                print(f"[!] Неизвестный OID: {oid.hex()}")
                continue  # Пропускаем, не отправляем ответ

            # ------------------------------------------------------------------
            # Отправка ответа клиенту
            # ------------------------------------------------------------------
            # Отправляем на тот же адрес, с которого пришёл запрос
            sock.sendto(response, addr)

except KeyboardInterrupt:
    # Пользователь нажал Ctrl+C - корректное завершение
    print("\n[!] Агент остановлен")
    
finally:
    # Всегда закрываем сокет при выходе
    sock.close()