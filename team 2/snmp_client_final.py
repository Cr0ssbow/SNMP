UBUNTU_IP = '192.168.0.203'

# UDP порт, на котором слушает агент (стандартный SNMP порт 161, но мы используем 1161)
UBUNTU_PORT = 1161

# Community string - своего рода "пароль" для SNMP v1/v2c
# 'public' - стандартное значение для чтения (read-only)
COMMUNITY = 'public'

