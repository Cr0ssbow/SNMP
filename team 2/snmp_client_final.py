# ==============================================================================
# ОПРЕДЕЛЕНИЕ OID (Object Identifiers) В ФОРМАТЕ БАЙТ
# ==============================================================================
# 
# OID - это уникальный идентификатор переменной в SNMP.
# Формат: иерархическая последовательность чисел, разделённых точками.
#
# Структура наших OID:
#   1.3.6.1.4.1.12345.1.X.0
#   │ │ │ │ │ │     │ │ │ └─ Экземпляр (всегда 0 для скалярных значений)
#   │ │ │ │ │ │     │ │ └─── Номер переменной (1=message, 2=counter, 3=temp)
#   │ │ │ │ │ │     │ └───── Группа переменных (1)
#   │ │ │ │ │ │     └─────── Enterprise ID (наш: 12345)
#   │ │ │ │ │ └───────────── enterprises (1)
#   │ │ │ │ └─────────────── private (4)
#   │ │ │ └───────────────── internet (1)
#   │ │ └─────────────────── dod (6)
#   │ └───────────────────── org (3)
#   └─────────────────────── iso (1)
#
# BER кодирование OID:
#   - Первые два числа кодируются как: первое*40 + второе
#   - 1.3 → 1*40 + 3 = 43 = 0x2B
#   - Числа < 128 кодируются одним байтом
#   - Числа >= 128 кодируются несколькими байтами (high bit = 1 для продолжения)
#   - 12345 = 0x3039 → в BER: 0xE0, 0x79 (с битами продолжения)
# ==============================================================================

# OID для текстового сообщения: 1.3.6.1.4.1.12345.1.1.0
OID_MESSAGE = b'\x2b\x06\x01\x04\x01\xe0\x79\x01\x01\x00'

# OID для счётчика (число): 1.3.6.1.4.1.12345.1.2.0
OID_COUNTER = b'\x2b\x06\x01\x04\x01\xe0\x79\x01\x02\x00'

# OID для температуры (число): 1.3.6.1.4.1.12345.1.3.0
OID_TEMP = b'\x2b\x06\x01\x04\x01\xe0\x79\x01\x03\x00'

# ==============================================================================
# ГЛОБАЛЬНЫЙ СЧЁТЧИК REQUEST ID
# ==============================================================================
# Request ID - уникальный идентификатор запроса.
# Используется для сопоставления запроса и ответа.
# Инициализируется случайным числом для безопасности.
# ==============================================================================

request_id_counter = random.randint(1, 10000)


# ==============================================================================
# ФУНКЦИЯ: create_snmp_get(oid)
# ==============================================================================
def create_snmp_get(oid):
    """
    Создаёт SNMP GET запрос в бинарном формате.
    
    Структура SNMP сообщения (BER/ASN.1 кодирование):
    ┌─────────────────────────────────────────────────────────┐
    │ SEQUENCE (0x30)                                         │
    │   ├─ INTEGER version (0x02) = 0 (SNMPv1)               │
    │   ├─ OCTET STRING community (0x04) = "public"          │
    │   └─ PDU GET-REQUEST (0xA0)                            │
    │        ├─ INTEGER request-id (0x02)                    │
    │        ├─ INTEGER error-status (0x02) = 0              │
    │        ├─ INTEGER error-index (0x02) = 0               │
    │        └─ SEQUENCE variable-bindings (0x30)            │
    │             └─ SEQUENCE varbind (0x30)                 │
    │                  ├─ OBJECT IDENTIFIER (0x06) = OID     │
    │                  └─ NULL (0x05 0x00)                   │
    └─────────────────────────────────────────────────────────┘
    
    Args:
        oid (bytes): OID переменной в BER-кодированном формате
        
    Returns:
        bytes: Полный SNMP GET запрос, готовый к отправке по UDP
        
    Пример:
        >>> request = create_snmp_get(OID_MESSAGE)
        >>> sock.sendto(request, (ip, port))
    """
    
    # Используем глобальный счётчик для уникального Request ID
    global request_id_counter
    request_id_counter += 1  # Инкремент для каждого нового запроса

    # --------------------------------------------------------------------------
    # Формируем Request ID
    # --------------------------------------------------------------------------
    # Формат: [тег INTEGER 0x02][длина 0x04][4 байта числа big-endian]
    # struct.pack('>I', ...) - упаковывает число как 4-байтовый unsigned int
    # '>' означает big-endian (старший байт первый)
    request_id = b'\x02\x04' + struct.pack('>I', request_id_counter)

    # --------------------------------------------------------------------------
    # Формируем VarBind (пара OID + значение)
    # --------------------------------------------------------------------------
    # Для GET запроса значение = NULL (0x05 0x00)
    # Формат: SEQUENCE { OID, NULL }
    # 0x30 = тег SEQUENCE
    # len(oid) + 4 = длина OID + 2 байта тега OID + 2 байта NULL
    varbind = b'\x30' + bytes([len(oid) + 4]) + \
              b'\x06' + bytes([len(oid)]) + oid + \
              b'\x05\x00'  # NULL value (тег 0x05, длина 0x00)

    # --------------------------------------------------------------------------
    # Формируем VarBindList (список VarBind)
    # --------------------------------------------------------------------------
    # В нашем случае только один VarBind
    varbindlist = b'\x30' + bytes([len(varbind)]) + varbind

    # --------------------------------------------------------------------------
    # Формируем PDU (Protocol Data Unit)
    # --------------------------------------------------------------------------
    # error-status = 0 (noError) - для запроса всегда 0
    error_status = b'\x02\x01\x00'
    # error-index = 0 - для запроса всегда 0
    error_index = b'\x02\x01\x00'

    # Собираем содержимое PDU
    pdu_data = request_id + error_status + error_index + varbindlist
    
    # 0xA0 = тег GET-REQUEST PDU (context-specific, constructed, tag 0)
    pdu = b'\xa0' + bytes([len(pdu_data)]) + pdu_data

    # --------------------------------------------------------------------------
    # Формируем полное SNMP сообщение
    # --------------------------------------------------------------------------
    # version = 0 для SNMPv1, 1 для SNMPv2c
    version = b'\x02\x01\x00'  # INTEGER 0 = SNMPv1
    
    # Community string в формате OCTET STRING
    community_bytes = COMMUNITY.encode('utf-8')  # Конвертируем строку в байты
    community = b'\x04' + bytes([len(community_bytes)]) + community_bytes

    # Собираем всё вместе
    message_data = version + community + pdu
    
    # Оборачиваем в SEQUENCE (тег 0x30)
    message = b'\x30' + bytes([len(message_data)]) + message_data

    return message


# ==============================================================================
# ФУНКЦИЯ: parse_snmp_response(data)
# ==============================================================================
def parse_snmp_response(data):
    """
    Парсит SNMP GET-RESPONSE и извлекает значение переменной.
    
    Структура ответа аналогична запросу, но:
    - PDU тег = 0xA2 (GET-RESPONSE)
    - Вместо NULL содержится реальное значение (STRING или INTEGER)
    
    Алгоритм парсинга (упрощённый):
    1. Ищем OCTET STRING (0x04) с конца пакета - для текстовых значений
    2. Если не нашли, ищем INTEGER (0x02 0x01) - для числовых значений
    3. Фильтруем "public" (это community string, не наше значение)
    
    Args:
        data (bytes): Полученный UDP пакет с SNMP ответом
        
    Returns:
        str: Извлечённое значение в виде строки
             или "Не распознано" если парсинг не удался
             или "Ошибка: ..." в случае исключения
             
    Пример:
        >>> data, _ = sock.recvfrom(1024)
        >>> value = parse_snmp_response(data)
        >>> print(value)  # "Ubuntu | 17:42:34" или "25"
    """
    try:
        # ----------------------------------------------------------------------
        # Поиск текстового значения (OCTET STRING)
        # ----------------------------------------------------------------------
        # Ищем с конца пакета, потому что community string "public" тоже
        # закодирован как OCTET STRING, но находится в начале пакета.
        # Наше значение всегда в конце (в VarBind).
        
        for i in range(len(data) - 2, 10, -1):  # Идём с конца к началу
            if data[i] == 0x04:  # Нашли тег OCTET STRING
                length = data[i + 1]  # Следующий байт = длина строки
                
                # Проверяем, что данные не выходят за пределы пакета
                if length > 0 and i + 2 + length <= len(data):
                    try:
                        # Извлекаем и декодируем строку
                        value = data[i + 2:i + 2 + length].decode('utf-8')
                        
                        # Фильтруем community string и слишком короткие строки
                        if 'public' not in value and len(value) > 3:
                            return value
                    except:
                        pass  # Если не удалось декодировать - пробуем дальше

        # ----------------------------------------------------------------------
        # Поиск числового значения (INTEGER)
        # ----------------------------------------------------------------------
        # Формат INTEGER: [0x02][длина][значение]
        # Для чисел < 128: длина = 0x01, значение = 1 байт
        
        for i in range(len(data) - 3, 15, -1):  # Ищем с конца
            if data[i:i+2] == b'\x02\x01':  # INTEGER с длиной 1 байт
                value = data[i + 2]  # Само значение
                
                # Пропускаем error-status и error-index (они тоже INTEGER 0)
                # Они находятся ближе к началу PDU
                if value > 0 or i > len(data) - 10:
                    return str(value)

        # ----------------------------------------------------------------------
        # Если ничего не нашли
        # ----------------------------------------------------------------------
        return "Не распознано"

    except Exception as e:
        return f"Ошибка: {e}"


# ==============================================================================
# ФУНКЦИЯ: snmp_get(oid, desc)
# ==============================================================================
def snmp_get(oid, desc):
    """
    Высокоуровневая функция для выполнения SNMP GET запроса.
    
    Выполняет полный цикл:
    1. Создаёт UDP сокет
    2. Формирует SNMP GET запрос
    3. Отправляет запрос серверу
    4. Ожидает ответ (с таймаутом)
    5. Парсит ответ и извлекает значение
    6. Выводит результат в консоль
    
    Args:
        oid (bytes): OID переменной в BER формате (например, OID_MESSAGE)
        desc (str): Человекочитаемое описание для вывода (например, "Сообщение")
        
    Returns:
        str or None: Полученное значение или None при ошибке
        
    Пример:
        >>> snmp_get(OID_MESSAGE, 'Сообщение')
        [→] Запрашиваю: Сообщение
        [✓] Получено: Ubuntu | 17:42:34
    """
    
    print(f"\n[→] Запрашиваю: {desc}")

    # --------------------------------------------------------------------------
    # Создание UDP сокета
    # --------------------------------------------------------------------------
    # SOCK_DGRAM = UDP (без установления соединения)
    # AF_INET = IPv4
    sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    
    # Устанавливаем таймаут 2 секунды
    # Если за это время ответ не придёт - возникнет исключение socket.timeout
    sock.settimeout(2.0)

    try:
        # ----------------------------------------------------------------------
        # Отправка запроса
        # ----------------------------------------------------------------------
        request = create_snmp_get(oid)  # Формируем SNMP пакет
        sock.sendto(request, (UBUNTU_IP, UBUNTU_PORT))  # Отправляем по UDP

        # ----------------------------------------------------------------------
        # Получение ответа
        # ----------------------------------------------------------------------
        # recvfrom возвращает кортеж (данные, адрес_отправителя)
        # Максимальный размер буфера 1024 байта (достаточно для SNMP)
        data, _ = sock.recvfrom(1024)
        
        # ----------------------------------------------------------------------
        # Парсинг ответа
        # ----------------------------------------------------------------------
        result = parse_snmp_response(data)

        print(f"[✓] Получено: {result}")
        return result

    except socket.timeout:
        # Сервер не ответил за 2 секунды
        print("[✗] Таймаут!")
        return None
        
    except Exception as e:
        # Любая другая ошибка (сеть недоступна, etc.)
        print(f"[✗] Ошибка: {e}")
        return None
        
    finally:
        # Всегда закрываем сокет (даже при ошибке)
        sock.close()


# ==============================================================================
# ГЛАВНЫЙ ЦИКЛ ПРОГРАММЫ - ИНТЕРАКТИВНОЕ МЕНЮ
# ==============================================================================
# Бесконечный цикл, который показывает меню и обрабатывает выбор пользователя.
# Завершается при выборе опции "0" (Выход).
# ==============================================================================

while True:
    # --------------------------------------------------------------------------
    # Отображение меню
    # --------------------------------------------------------------------------
    print("\n" + "="*60)
    print("1. Получить сообщение")      # Запросить OID_MESSAGE
    print("2. Получить счётчик")         # Запросить OID_COUNTER
    print("3. Получить температуру")     # Запросить OID_TEMP
    print("4. Мониторинг (каждые 3 сек)") # Непрерывный опрос всех OID
    print("0. Выход")                     # Завершение программы
    print("="*60)

    # --------------------------------------------------------------------------
    # Получение выбора пользователя
    # --------------------------------------------------------------------------
    choice = input("Выбери: ").strip()  # strip() убирает пробелы по краям

    # --------------------------------------------------------------------------
    # Обработка выбора
    # --------------------------------------------------------------------------
    
    if choice == '1':
        # Запрос текстового сообщения
        snmp_get(OID_MESSAGE, 'Сообщение')
        
    elif choice == '2':
        # Запрос счётчика
        snmp_get(OID_COUNTER, 'Счётчик')
        
    elif choice == '3':
        # Запрос температуры
        snmp_get(OID_TEMP, 'Температура')
        
    elif choice == '4':
        # ----------------------------------------------------------------------
        # Режим мониторинга - непрерывный опрос
        # ----------------------------------------------------------------------
        print("\n[!] Мониторинг запущен (Ctrl+C для остановки)")
        
        try:
            while True:  # Бесконечный цикл мониторинга
                print("\n" + "─"*60)  # Разделитель между итерациями
                
                # Запрашиваем все три значения
                snmp_get(OID_MESSAGE, 'Сообщение')
                snmp_get(OID_COUNTER, 'Счётчик')
                snmp_get(OID_TEMP, 'Температура')
                
                # Пауза 3 секунды между запросами
                time.sleep(3)
                
        except KeyboardInterrupt:
            # Пользователь нажал Ctrl+C
            print("\n[!] Мониторинг остановлен")
            
    elif choice == '0':
        # Выход из программы
        print("Выход!")
        break  # Прерывает цикл while True
        
    # Если введено что-то другое - просто показываем меню снова